package loopDemo;

public class LoopExercise {


    public void whileloop() {
        int a=10;
        while (a<=50){
            System.out.println(a);
            a++;
        }
    }

    public static void main(String[] args) {
        LoopExercise obj= new LoopExercise();
        obj.whileloop();
    }

}
